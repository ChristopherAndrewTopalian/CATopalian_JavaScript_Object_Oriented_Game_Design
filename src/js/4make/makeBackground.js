// makeBackground.js

function makeBackground()
{
    let theBackground = ce('img');
    theBackground.id = 'theBackground';
    theBackground.style.backgroundColor = 'rgb(0, 0, 0)';
    theBackground.src = 'src/media/texture/background/001.jpg';
    theBackground.style.position = 'absolute';
    theBackground.style.left = 0 + 'px';
    theBackground.style.top = 0 + 'px';
    theBackground.style.width = '1000px';
    ba(theBackground);
}

